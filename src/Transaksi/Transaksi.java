    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // cari tagihan
        String id = id_tagihan.getText();
        Model model = new Model();
        ResultSet res =  model.getDataById("tagihan", "id", id);
        try {
            if (res.next()) {
                total.setText(res.getString("total"));
                tanggal.setText(res.getString("tanggal"));
                id_tagihan.setEditable(false);
                tanggal_bayar.setEditable(true);
                bayar.setEditable(true);
                bayar_button.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Tagihan tidak ditemukan");
            }
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

