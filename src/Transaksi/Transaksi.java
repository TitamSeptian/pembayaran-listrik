    private void loadTable(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Tagihan");
        model.addColumn("Tanggal Bayar");
        model.addColumn("Bayar");
        model.addColumn("Kembalian");
        try{
            
            Model TransaksiModel = new Model();
            ResultSet res = TransaksiModel.getAll("transaksi");
            while(res.next()){
                model.addRow(new Object[]{res.getString("id_tagihan"), res.getString("tanggal_bayar"), res.getString("bayar"), res.getString("kembalian")});
            }
            jTable1.setModel(model);
        }catch (HeadlessException | SQLException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this,"Kesalahan Combo :  "+  e.getMessage());
        }
    }
    
    private void loadComboPetugas(){
        try{
            Model model = new Model();
            ResultSet res = model.getAll("petugas");
            while(res.next()){
                comboPetugas.addItem(res.getString("kode_petugas"));
            }
        }catch (HeadlessException | SQLException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this,"Kesalahan Combo :  "+  e.getMessage());
        }
    }

