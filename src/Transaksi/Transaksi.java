    private void bayar_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayar_buttonActionPerformed
//        bayar
        Model model = new Model();
        ArrayList<String> data = new ArrayList<String>();
        ArrayList<String> column = new ArrayList<String>();
        ArrayList<String> dataTagihan = new ArrayList<String>();
        ArrayList<String> columnTagihan = new ArrayList<String>();
        int totalTagihan = Integer.parseInt(total.getText());
        int bayarTagihan = Integer.parseInt(bayar.getText());
        String idTagihan = id_tagihan.getText();
        ResultSet resTagihan = model.getDataById("tagihan", "id", idTagihan);
        try {
            if (resTagihan.next()){
                if(resTagihan.getString("status").equals("selesai")) {
                    JOptionPane.showMessageDialog(this, "Tagihan Sudah dibayar");
                } else {
                    if (bayarTagihan < totalTagihan) {
                        JOptionPane.showMessageDialog(null, "Uang yang anda masukkan kurang");
                    } else {
                        int kembalianTagihan = bayarTagihan - totalTagihan;
                        kembalian.setText(String.valueOf(kembalianTagihan));
                        column.add("id_tagihan");data.add(id_tagihan.getText());
                        column.add("bayar");data.add(bayar.getText());
                        column.add("kembalian");data.add(Integer.toString(kembalianTagihan));
                        column.add("tanggal_bayar");data.add(tanggal_bayar.getText());
                        column.add("id_petugas");data.add(comboPetugas.getSelectedItem().toString());
                        boolean insert = model.insertSet(data, column, "transaksi");

                        columnTagihan.add("status");dataTagihan.add("selesai");
                        boolean update = model.update(dataTagihan, columnTagihan, "tagihan", "id", id_tagihan.getText());
                        if(insert && update){
                            kosong();
                            loadTable();
                        }
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_bayar_buttonActionPerformed

